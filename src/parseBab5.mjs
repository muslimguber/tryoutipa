const questions = [];
const lines = rawText.split('\n').map(l => l.trim()).filter(l => l);

let currentQ = null;
let idCounter = 1;

for (const line of lines) {
  if (line.match(/^\d+\./)) {
    if (currentQ) questions.push(currentQ);
    currentQ = { id: idCounter++, question: line.replace(/^\d+\.\s*/, ''), options: [], correctId: '' };
  } else if (line.match(/^[a-d]\./)) {
    const id = line[0];
    const text = line.replace(/^[a-d]\.\s*/, '');
    currentQ.options.push({ id, text });
  } else if (line.startsWith('Jawaban:')) {
    const match = line.match(/Jawaban:\s*([a-d])\./);
    if (match) {
      currentQ.correctId = match[1];
    }
  }
}
if (currentQ) questions.push(currentQ);

const chunk = (arr, size) => Array.from({ length: Math.ceil(arr.length / size) }, (v, i) =>
  arr.slice(i * size, i * size + size)
);

const chunks = chunk(questions, 20);

if (!fs.existsSync(process.cwd() + '/src/data')) {
  fs.mkdirSync(process.cwd() + '/src/data');
}

chunks.forEach((chunkData, index) => {
  const content = `export const KUIS${index + 1}_BAB5_QUESTIONS = ${JSON.stringify(chunkData, null, 2)};\n`;
  fs.writeFileSync(process.cwd() + `/src/data/Kuis${index + 1}Bab5.ts`, content);
});

console.log('Parsed successfully to 4 files.');
